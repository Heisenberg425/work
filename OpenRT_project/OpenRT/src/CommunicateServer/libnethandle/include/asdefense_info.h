#ifndef __ASDEFENSE_INFO_H
#define __ASDEFENSE_INFO_H
#include "libasdefense_config.h"

/*********************************************************************************************************
** 函数名称: libAsDefenseHandle
** 功能描述: 使能 / 禁能 ARP 欺骗防御
** 输　入  : bEnFlag            是否允许开启
** 输　出  : NONE
** 返　回  : ERROR_CODE
*********************************************************************************************************/
INT  libAsDefenseHandle (BOOL  bEnFlag);

/*********************************************************************************************************
** 函数名称: libAsDefenseNumGet
** 功能描述:  ARP 防御信息个数获取
** 输　入  : ucType             信息类型
** 输　出  : NONE
** 返　回  : ERROR_CODE
*********************************************************************************************************/
INT  libAsDefenseNumGet (UINT8  ucType);

/*********************************************************************************************************
** 函数名称: libAsDefenseStatusGet
** 功能描述: ARP 防御开启状态获取
** 输　入  : NONE
** 输　出  : NONE
** 返　回  : 开启状态
*********************************************************************************************************/
INT  libAsDefenseStatusGet (VOID);

/*********************************************************************************************************
** 函数名称: libAsDefenseInfoGet
** 功能描述: 获取 ARP 防御信息
** 输　入  : pGetHandle         arp 防御获取信息操作
** 输　出  : piTotal            条目总数
** 返　回  : ARP 防御信息
*********************************************************************************************************/
PVOID  libAsDefenseInfoGet (__PASDEFENSE_GET  pGetHandle, INT32 *piTotal);
#endif                                                                  /*  __ASDEFENSE_INFO_H          */
